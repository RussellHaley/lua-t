t=require('t')
fmt=string.format

b = 0xABCD
l = 0xCDAB
print( fmt('%016X    %016X',b,l) )

p = t.Pack( 'f' )
s = string.char( 0x23, 0x34, 0x45, 0x56, 0x67, 0x78, 0x89, 0xEF )
s = string.char( 0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF )

lf = string.unpack( 'f', s )
lt = p(s)

print( lf )
print( lt )
